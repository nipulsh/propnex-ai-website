import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const schemaDir = path.join(__dirname, "../src/server/graphql/schema");
const outFile = path.join(__dirname, "../src/server/graphql/type-defs.generated.ts");

const files = [
  "root.graphql",
  "credits.graphql",
  "billing.graphql",
  "call-logs.graphql",
  "domains.graphql",
  "agent-library.graphql",
];

const typeDefs = files.map((file) =>
  fs.readFileSync(path.join(schemaDir, file), "utf8"),
);

const output = `// Auto-generated by scripts/bundle-graphql-schema.mjs — do not edit manually
export const typeDefs = [
${typeDefs.map((doc) => `  ${JSON.stringify(doc)}`).join(",\n")}
] as const;
`;

fs.writeFileSync(outFile, output);
console.log(`Wrote ${outFile} (${files.length} schema files)`);
